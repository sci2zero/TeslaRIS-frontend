<template>
    <v-menu v-model="isMenuOpen" :close-on-content-click="false">
        <template #activator="{ props }">
            <v-text-field
                :label="label"
                :model-value="formattedDate"
                :rules="required ? requiredFieldRules : []"
                readonly
                v-bind="props"
                variant="solo"
                hide-details
            ></v-text-field>
        </template>
        <v-date-picker v-model="selectedDate" hide-actions title="" :color="color">
            <template #header></template>
            <template #actions>
                <v-btn @click="clearDate">
                    {{ $t("deleteLabel") }}
                </v-btn>
                <v-btn @click="isMenuOpen = false">
                    {{ $t("saveLabel") }}
                </v-btn>
            </template>
        </v-date-picker>
    </v-menu>
</template>

<script lang="ts">
import { useValidationUtils } from "@/utils/ValidationUtils";
import type { PropType } from "vue";
import { ref, computed, watch, defineComponent } from "vue";

export default defineComponent({
  name: "DatePicker",
  props: {
    label: {
      type: String,
      required: true,
    },
    color: {
      type: String,
      required: true,
    },
    required: {
      type: Boolean,
      default: false
    },
    modelValue: {
      type: Object as PropType<string | undefined>,
      required: true,
    },
  },
  emits: ["update:modelValue"],
  setup(props, { emit }) {
    const isMenuOpen = ref(false);
    const selectedDate = ref(props.modelValue ? new Date(props.modelValue) : undefined);

    const { requiredFieldRules } = useValidationUtils();

    const toIsoString = (date: Date) => {
      const pad = function(num : number) {
              return (num < 10 ? '0' : '') + num;
        };

        return date.getFullYear() +
            '-' + pad(date.getMonth() + 1) +
            '-' + pad(date.getDate()) +
            'T' + pad(date.getHours()) +
            ':' + pad(date.getMinutes()) +
            ':' + pad(date.getSeconds());
    };


    const formattedDate = computed(() => {
      return selectedDate.value ? new Date(selectedDate.value).toLocaleDateString("sr") : "";
    });

    const clearDate = () => {
      selectedDate.value = undefined;
    };

    watch(() => props.modelValue, (newDate) => {
      if(newDate) {
        selectedDate.value = new Date(newDate);
      }
    });

    watch(selectedDate, (newDate) => {
      if (newDate) {
        emit("update:modelValue", toIsoString(newDate));
      } else {
        emit("update:modelValue", "");
      }
    });

    return {
      isMenuOpen,
      selectedDate,
      formattedDate,
      requiredFieldRules,
      clearDate
    };
  },
});
</script>


<style>
.v-overlay__content:has(> .v-date-picker) {
    min-width: auto!important;
}
.v-picker-title {
    padding: 0 !important;
}
</style>`
